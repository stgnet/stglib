class StFont:public StBitmap
{
public:
	StSize xsize;
	StSize ysize;

	StFont(const char *s,StColor fg=StColor_White,StColor bg=StColor_Black)
	{
		StSize len=strlen(s);
		_InitializeImage(6*len,8);
		
		StSize ci=0;
		while (ci<len)
		{
			StByte ch=s[ci];
			if (ch>127)
				ch=0;

			StSize yi=0;
			while (yi<8)
			{
				StByte image=StFont_6x8[ch][yi];

				StSize x=6*ci+5;

				StSize xi=0;
				while (xi<6)
				{
					(*this)(x,yi)=(image&1)?fg:bg;
					--x;
					++xi;
					image>>=1;
				}
				++yi;
			}
			++ci;
		}
	}

	~StFont()
	{
	}
};

